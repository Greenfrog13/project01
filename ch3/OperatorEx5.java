class OperatorEx5 {
	public static void main(String[] args){
		int a = 10;
		int b = 4;
		
		System.out.println("%d + %d = %d\n", a, b, a+b);
		System.out.println("%d - %d = %d\n", a, b, a-b);
		System.out.println("%d * %d = %d\n", a, b, a*b);
		System.out.println("%d / %d = %d\n", a, b, a/b);
		System.out.println("%d / %f = %d\n", a, (float)b, a/(float)b);
	}
}
		
